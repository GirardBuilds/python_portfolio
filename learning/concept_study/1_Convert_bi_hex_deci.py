# Convert TO other bases from decimal
bin(255)        # '0b11111111' - decimal to binary
hex(255)        # '0xff' - decimal to hex
bin(255)[2:]    # '11111111' - strip prefix
hex(255)[2:]    # 'ff' - strip prefix

# Convert FROM other bases to decimal
int('11111111', 2)   # 255 - binary to decimal
int('ff', 16)        # 255 - hex to decimal
int('255')           # 255 - decimal stays decimal

